        "    eloss = 0.0\n"
        "    for i in range(0, len(X_tr), 64):\n"
        "        idx = perm[i:i+64]\n"
        "        opt3.zero_grad()\n"
        "        loss = crit(qnn(X_tr[idx]), y_tr[idx])\n"
        "        loss.backward()\n"
        "        opt3.step()\n"
        "        eloss += loss.item()\n"
        "    sched.step()\n"
        "    if epoch % 10 == 0 or epoch == 1:\n"
        "        qnn.eval()\n"
        "        with torch.no_grad():\n"
