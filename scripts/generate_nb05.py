        "# MODEL 1: Pure VQC (AngleEmbedding + StronglyEntanglingLayers)\n"
        "# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n"
        "print('='*60)\n"
