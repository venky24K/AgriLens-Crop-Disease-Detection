        "idx_te = np.random.choice(len(X_test_s), N_TE, replace=False)\n"
        "Xk_tr, yk_tr = X_train_s[idx_tr], y_train[idx_tr]\n"
        "Xk_te, yk_te = X_test_s[idx_te], y_test[idx_te]\n"
        "\n"
