        "print('='*60)\n"
        "print('MODEL 3: Data Re-uploading QNN (8 layers)')\n"
        "print('='*60)\n"
        "\n"
        "n_l3 = 8\n"
        "dev3 = qml.device('default.qubit', wires=n_features)\n"
        "\n"
        "@qml.qnode(dev3, interface='torch', diff_method='backprop')\n"
        "def reupload_circuit(inputs, weights):\n"
        "    for layer in range(n_l3):\n"
        "        qml.AngleEmbedding(inputs, wires=range(n_features), rotation='Y')\n"
        "        for q in range(n_features):\n"
        "            qml.Rot(weights[layer,q,0], weights[layer,q,1], weights[layer,q,2], wires=q)\n"
        "        for q in range(n_features):\n"
        "            qml.CNOT(wires=[q, (q+1)%n_features])\n"
        "        if n_features >= 4:\n"
        "            for q in range(0, n_features-2, 2):\n"
        "                qml.CNOT(wires=[q, q+2])\n"
